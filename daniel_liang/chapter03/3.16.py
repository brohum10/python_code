import turtle
t = turtle.Turtle()
t.speed(0)

t.up()
t.goto(-400,0)
t.down()

t.begin_fill()
t.color("red")
t.circle(50, steps = 3)
t.end_fill()

t.up()
t.goto(-250, 0)
t.down()
t.color("orange")
t.begin_fill()
t.circle(50, steps = 4)
t.end_fill()

t.up()
t.goto(-100, 0)
t.down()
t.color("yellow")
t.begin_fill()
t.circle(50, steps = 5)
t.end_fill()

t.up()
t.goto(50, 0)
t.down()
t.color("green")
t.begin_fill()
t.circle(50, steps = 6)
t.end_fill()

t.up()
t.goto(200, 0)
t.down()
t.color("blue")
t.begin_fill()
t.circle(50, steps = 7)
t.end_fill()

t.up()
t.goto(-150, 0)
t.lt(90)
t.fd(200)
t.write("Cool Colorful Shapes", font = 50)
t.hideturtle()
